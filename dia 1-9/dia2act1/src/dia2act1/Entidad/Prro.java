package dia2act1.Entidad;

public class Prro {
   
    private String raza;
    private String name;

    public Prro() {
    }

    public Prro(String raza, String name) {
        this.raza = raza;
        this.name = name;
    }

    public String getRaza() {
        return raza;
    }

    public void setRaza(String raza) {
        this.raza = raza;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
